public class SpecialAgreement {
    private String Refused;
    private String Denied;

    public String getRefused() {
        return Refused;
    }

    public void setRefused(String refused) {
        Refused = refused;
    }

    public String getDenied() {
        return Denied;
    }

    public void setDenied(String denied) {
        Denied = denied;
    }
}
